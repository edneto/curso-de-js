const grade = 'B'

//verifica múltiplos valores possíveis de uma variável ou constante
switch (grade) {
    case 'A':
        console.log('Você tirou um A')
        break;//quebra a continuação do código
    case 'B':
        console.log('Você tirou um B')
        break

    case 'C':
        console.log('Você tirou um C')
        break

    case 'D':
        console.log('Você tirou um D')
        break

    case 'E':
        console.log('Você tirou um E')
        break

    default:
        console.log('nota inválida')
}